package tcc.dtos;

public class TB_PRODUTOS {
	
	private int ID_PRODUTO;
	private String COD_ALTERNATIVA;
	private String PRODUTO_DESC;
	
	public int getID_PRODUTO() {
		return ID_PRODUTO;
	}
	public void setID_PRODUTO(int iD_PRODUTO) {
		ID_PRODUTO = iD_PRODUTO;
	}
	public String getCOD_ALTERNATIVA() {
		return COD_ALTERNATIVA;
	}
	public void setCOD_ALTERNATIVA(String cOD_ALTERNATIVA) {
		COD_ALTERNATIVA = cOD_ALTERNATIVA;
	}
	public String getPRODUTO_DESC() {
		return PRODUTO_DESC;
	}
	public void setPRODUTO_DESC(String pRODUTO_DESC) {
		PRODUTO_DESC = pRODUTO_DESC;
	}
	
	

}
